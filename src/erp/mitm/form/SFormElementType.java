    @Override
    public void setRegistry(erp.lib.data.SDataRegistry registry) {
        moElementType = (SDataElementType) registry;

        moFieldElementType.setFieldValue(moElementType.getElementType());
        moFieldIsDeleted.setFieldValue(moElementType.getIsDeleted());

        if (moElementType.getIsCanDelete()) {
            jckIsDeleted.setEnabled(true);
        }
    }

    @Override
    public erp.lib.data.SDataRegistry getRegistry() {
        if (moElementType == null) {
            moElementType = new SDataElementType();
            moElementType.setFkUserNewId(miClient.getSession().getUser().getPkUserId());
        }
        else {
            moElementType.setFkUserEditId(miClient.getSession().getUser().getPkUserId());
        }

        moElementType.setElementType(moFieldElementType.getString());
        moElementType.setIsCanEdit(true);
        moElementType.setIsCanDelete(true);
        moElementType.setIsDeleted(moFieldIsDeleted.getBoolean());

        return moElementType;
    }

