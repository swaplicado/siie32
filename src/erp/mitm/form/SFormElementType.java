    @Override
    public erp.lib.form.SFormValidation formValidate() {
        SFormValidation validation = new SFormValidation();

        for (int i = 0; i < mvFields.size(); i++) {
            if (!((erp.lib.form.SFormField) mvFields.get(i)).validateField()) {
                validation.setIsError(true);
                validation.setComponent(((erp.lib.form.SFormField) mvFields.get(i)).getComponent());
                break;
            }
        }

        if (!validation.getIsError()) {
            Object[] oParamsIn = new Object[] { moElementType == null ? 0 : moElementType.getPkElementTypeId(), moFieldElementType.getString() };

            if ( SDataUtilities.callProcedureVal(miClient, SProcConstants.ITMU_TP_EMT_VAL, oParamsIn, SLibConstants.EXEC_MODE_VERBOSE) > 0 ) {
                if (miClient.showMsgBoxConfirm("El valor del campo '" + jlElementType.getText() + "' ya existe, Â¿desea conservalo? ") == JOptionPane.NO_OPTION) {
                    validation.setComponent(jtfElementType);
                    validation.setIsError(true);
                }
            }
        }

        return validation;
    }

