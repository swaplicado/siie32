    @Override
    public void setRegistry(erp.lib.data.SDataRegistry registry) {
        moItemSupplier = (SDataItemSupplier) registry;

        moFieldPkBizPartnerId.setFieldValue(new int[] { moItemSupplier.getPkBizPartnerId()});
        moFieldPkItemId.setFieldValue(new int[] { moItemSupplier.getPkItemId()});
        moFieldProductionLine.setFieldValue(moItemSupplier.getProductionLine());
        moFieldIsDeleted.setFieldValue(moItemSupplier.getIsDeleted());
        jtfPkItemBizPartnerId_Ro.setText(SLibUtils.textKey((int[]) moItemSupplier.getPrimaryKey()));

        jckIsDeleted.setEnabled(true);
    }

    @Override
    public erp.lib.data.SDataRegistry getRegistry() {
        if (moItemSupplier == null) {
            moItemSupplier = new SDataItemSupplier();
            moItemSupplier.setFkUserNewId(miClient.getSession().getUser().getPkUserId());
        }
        else {
            moItemSupplier.setFkUserEditId(miClient.getSession().getUser().getPkUserId());
        }

        moItemSupplier.setPkBizPartnerId(moFieldPkBizPartnerId.getKeyAsIntArray()[0]);
        moItemSupplier.setPkItemId(moFieldPkItemId.getKeyAsIntArray()[0]);
        moItemSupplier.setProductionLine(moFieldProductionLine.getString());
        moItemSupplier.setIsDeleted(moFieldIsDeleted.getBoolean());

        return moItemSupplier;
    }

