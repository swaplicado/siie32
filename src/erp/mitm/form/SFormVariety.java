    @Override
    public void setRegistry(erp.lib.data.SDataRegistry registry) {
        moVariety = (SDataVariety) registry;

        moFieldFkVarietyTypeId.setFieldValue(new int[] { moVariety.getFkVarietyTypeId() });
        moFieldVariety.setFieldValue(moVariety.getVariety());
        moFieldCode.setFieldValue(moVariety.getCode());
        moFieldIsDeleted.setFieldValue(moVariety.getIsDeleted());

        if (moVariety.getIsCanDelete()) {
            jckIsDeleted.setEnabled(true);
        }
    }

    @Override
    public erp.lib.data.SDataRegistry getRegistry() {
        if (moVariety == null) {
            moVariety = new SDataVariety();
            moVariety.setFkUserNewId(miClient.getSession().getUser().getPkUserId());
        }
        else {
            moVariety.setFkUserEditId(miClient.getSession().getUser().getPkUserId());
        }

        moVariety.setFkVarietyTypeId(moFieldFkVarietyTypeId.getKeyAsIntArray()[0]);
        moVariety.setVariety(moFieldVariety.getString());
        moVariety.setCode(moFieldCode.getString());
        moVariety.setIsCanEdit(true);
        moVariety.setIsCanDelete(true);
        moVariety.setIsDeleted(moFieldIsDeleted.getBoolean());

        return moVariety;
    }

