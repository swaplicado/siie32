    @Override
    public void setRegistry(erp.lib.data.SDataRegistry registry) {
        moBrandType = (SDataBrandType) registry;

        moFieldBrandType.setFieldValue(moBrandType.getBrandType());
        moFieldIsDeleted.setFieldValue(moBrandType.getIsDeleted());

        if (moBrandType.getIsCanDelete()) {
            jckIsDeleted.setEnabled(true);
        }
    }

    @Override
    public erp.lib.data.SDataRegistry getRegistry() {
        if (moBrandType == null) {
            moBrandType = new SDataBrandType();
            moBrandType.setFkUserNewId(miClient.getSession().getUser().getPkUserId());
        }
        else {
            moBrandType.setFkUserEditId(miClient.getSession().getUser().getPkUserId());
        }

        moBrandType.setBrandType(moFieldBrandType.getString());
        moBrandType.setIsCanEdit(true);
        moBrandType.setIsCanDelete(true);
        moBrandType.setIsDeleted(moFieldIsDeleted.getBoolean());

        return moBrandType;
    }

