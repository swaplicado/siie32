    @Override
    public erp.lib.form.SFormValidation formValidate() {
        SFormValidation validation = new SFormValidation();

        for (int i = 0; i < mvFields.size(); i++) {
            if (!((erp.lib.form.SFormField) mvFields.get(i)).validateField()) {
                validation.setIsError(true);
                validation.setComponent(((erp.lib.form.SFormField) mvFields.get(i)).getComponent());
                break;
            }
        }

        if (!validation.getIsError()) {
            Object[] oParamsIn = new Object[] { moBrandType == null ? 0 : moBrandType.getPkBrandTypeId(), moFieldBrandType.getString() };

            if ( SDataUtilities.callProcedureVal(miClient, SProcConstants.ITMU_TP_BRD_VAL, oParamsIn, SLibConstants.EXEC_MODE_VERBOSE) > 0 ) {
                if (miClient.showMsgBoxConfirm("El valor del campo '" + jlBrandType.getText() + "' ya existe, Â¿desea conservalo? ") == JOptionPane.NO_OPTION) {
                    validation.setComponent(jtfBrandType);
                    validation.setIsError(true);
                }
            }
        }

        return validation;
    }

