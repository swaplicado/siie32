    @Override
    public void setRegistry(erp.lib.data.SDataRegistry registry) {
        moItemCustomer = (SDataItemCustomer) registry;

        moFieldPkBizPartnerId.setFieldValue(new int[] { moItemCustomer.getPkBizPartnerId()});
        moFieldPkItemId.setFieldValue(new int[] { moItemCustomer.getPkItemId()});
        moFieldNumberOfStores.setFieldValue(moItemCustomer.getNumberOfStores());
        moFieldNumberOfDistCenters.setFieldValue(moItemCustomer.getNumberOfDistCenters());
        moFieldIsDeleted.setFieldValue(moItemCustomer.getIsDeleted());
        jtfPkItemBizPartnerId_Ro.setText(SLibUtils.textKey((int[]) moItemCustomer.getPrimaryKey()));

        jcbPkBizPartnerId.setEnabled(false);
        jbPkBizPartnerId.setEnabled(false);
        jcbPkItemId.setEnabled(false);
        jbPkItemId.setEnabled(false);
        jckIsDeleted.setEnabled(true);
    }

    @Override
    public erp.lib.data.SDataRegistry getRegistry() {
        if (moItemCustomer == null) {
            moItemCustomer = new SDataItemCustomer();
            moItemCustomer.setFkUserNewId(miClient.getSession().getUser().getPkUserId());
        }
        else {
            moItemCustomer.setFkUserEditId(miClient.getSession().getUser().getPkUserId());
        }

        moItemCustomer.setPkBizPartnerId(moFieldPkBizPartnerId.getKeyAsIntArray()[0]);
        moItemCustomer.setPkItemId(moFieldPkItemId.getKeyAsIntArray()[0]);
        moItemCustomer.setNumberOfStores(moFieldNumberOfStores.getInteger());
        moItemCustomer.setNumberOfDistCenters(moFieldNumberOfDistCenters.getInteger());
        moItemCustomer.setIsDeleted(moFieldIsDeleted.getBoolean());

        return moItemCustomer;
    }

