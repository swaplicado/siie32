    private void renderItemGeneric() {
        if (moItemGeneric == null) {
            jtfItemGeneric.setText(SLibConstants.TXT_ALL);
        }
        else {
            jtfItemGeneric.setText(moItemGeneric.getItemGeneric());
        }

        jtfItemGeneric.setCaretPosition(0);
        jtfItemGeneric.setToolTipText(TXT_TIP + ": " + jtfItemGeneric.getText());
    }

    private void actionItemGeneric() {
        int[] key = null;

        moFilterItemGeneric.formReset();
        moFilterItemGeneric.formRefreshCatalogues();
        moFilterItemGeneric.setFormVisible(true);

        if (moFilterItemGeneric.getFormResult() == SLibConstants.FORM_RESULT_OK) {
            key = (int[]) moFilterItemGeneric.getValue(SDataConstants.ITMU_IGEN);
            moItemGeneric = (SDataItemGeneric) (key == null ? null : SDataUtilities.readRegistry(miClient, SDataConstants.ITMU_IGEN, key, SLibConstants.EXEC_MODE_SILENT));
            moTableTab.updateSetting(new STableSetting(STableConstants.SETTING_FILTER_ITM_ITEM_GENERIC, moItemGeneric == null ? new int[] { SLibConstants.UNDEFINED } : moItemGeneric.getPrimaryKey()));

            renderItemGeneric();
        }
    }

