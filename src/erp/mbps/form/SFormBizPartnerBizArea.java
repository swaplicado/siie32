    @Override
    public erp.lib.form.SFormValidation formValidate() {
        SFormValidation validation = new SFormValidation();

        for (int i = 0; i < mvFields.size(); i++) {
            if (!((erp.lib.form.SFormField) mvFields.get(i)).validateField()) {
                validation.setIsError(true);
                validation.setComponent(((erp.lib.form.SFormField) mvFields.get(i)).getComponent());
                break;
            }
        }

        if (!validation.getIsError()) {
            Object[] oParamsIn = new Object[] { moBizPartnerBizArea == null ? 0 : moBizPartnerBizArea.getPkBizAreaId(), moFieldBizArea.getString() };

            if (SDataUtilities.callProcedureVal(miClient, SProcConstants.BPSU_BA, oParamsIn, SLibConstants.EXEC_MODE_VERBOSE) > 0) {
                validation.setMessage("El valor del campo '" + jlBizArea.getText() + "' ya existe.");
                validation.setComponent(jtfBizArea);
            }
        }

        return validation;
    }

