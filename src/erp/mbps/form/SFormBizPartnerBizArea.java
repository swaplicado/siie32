    @Override
    public void setRegistry(erp.lib.data.SDataRegistry registry) {
        moBizPartnerBizArea = (SDataBizPartnerBizAreaCatalogue) registry;

        moFieldBizArea.setFieldValue(moBizPartnerBizArea.getBizArea());
        moFieldIsDeleted.setFieldValue(moBizPartnerBizArea.getIsDeleted());

        if (moBizPartnerBizArea.getIsCanDelete()) {
            jckIsDeleted.setEnabled(true);
        }
    }

    @Override
    public erp.lib.data.SDataRegistry getRegistry() {
        if (moBizPartnerBizArea == null) {
            moBizPartnerBizArea = new SDataBizPartnerBizAreaCatalogue();
            moBizPartnerBizArea.setFkUserNewId(miClient.getSession().getUser().getPkUserId());
        }
        else {
            moBizPartnerBizArea.setFkUserEditId(miClient.getSession().getUser().getPkUserId());
        }

        moBizPartnerBizArea.setBizArea(moFieldBizArea.getString());
        moBizPartnerBizArea.setIsCanEdit(true);
        moBizPartnerBizArea.setIsCanDelete(true);
        moBizPartnerBizArea.setIsDeleted(moFieldIsDeleted.getBoolean());

        return moBizPartnerBizArea;
    }

