    public void setCompanyBranchEntityKey(int[] key) {
        if (key == null) {
            mnCompanyBranchId = SLibConstants.UNDEFINED;
            mnEntityId = SLibConstants.UNDEFINED;

            jckAllCompanyBranch.setSelected(false);
            jckAllCompanyBranchItemStateChanged(null);

            moFieldCompanyBranchId.setKey(null);
            itemStateChangedCompanyBranch();

            jckAllEntity.setSelected(false);
            jckAllEntityItemStateChanged(null);

            moFieldEntity.setKey(null);
        }
        else {
            mnCompanyBranchId = key[0];
            mnEntityId = key[1];

            jckAllCompanyBranch.setSelected(mnCompanyBranchId == SLibConstants.UNDEFINED && isUniversalCompany());
            jckAllCompanyBranchItemStateChanged(null);

            moFieldCompanyBranchId.setKey(getCompanyBranchKey());
            itemStateChangedCompanyBranch();

            jckAllEntity.setSelected(mnEntityId == SLibConstants.UNDEFINED && isUniversalCompanyBranch() && !jckAllCompanyBranch.isSelected());
            jckAllEntityItemStateChanged(null);

            moFieldEntity.setKey(getCompanyBranchEntityKey());
        }
    }

    public int[] getCompanyBranchKey() {
        return new int[] { mnCompanyBranchId };
    }

    public int[] getCompanyBranchEntityKey() {
        return new int[] { mnCompanyBranchId, mnEntityId };
    }

