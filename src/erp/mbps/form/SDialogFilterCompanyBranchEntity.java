    private void windowActivated() {
        if (mbFirstTime) {
            mbFirstTime = false;

            if (jltEntity.isEnabled()) {
                jltEntity.requestFocus();
            }
            else if (jcbCompanyBranch.isEnabled()) {
                jcbCompanyBranch.requestFocus();
            }
            else {
                jbOk.requestFocus();
            }
        }
    }

    private boolean isUniversalCompany() {
        return jckUniversalCompany.isSelected();
    }

    private boolean isUniversalCompanyBranch() {
        return jckUniversalCompany.isSelected() || miClient.getSessionXXX().getIsUniversalCompanyBranch(moFieldCompanyBranchId.getKeyAsIntArray()[0]) || miClient.getSessionXXX().getIsUniversalCompanyBranchEntities(moFieldCompanyBranchId.getKeyAsIntArray()[0], mnEntityCategoryId);
    }

    private void itemStateChangedCompanyBranch() {
        boolean universal = false;
        Vector<SFormComponentItem> items = null;

        if (jcbCompanyBranch.getSelectedIndex() <= 0) {
            jckUniversalCompanyBranch.setSelected(false);

            jckAllEntity.setEnabled(false);
            jckAllEntity.setSelected(false);

            jlEntity.setEnabled(false);
            jltEntity.setEnabled(false);
            jltEntity.clearSelection();

            jltEntity.setListData(new SFormComponentItem[] {});
        }
        else {
            universal = isUniversalCompanyBranch();

            jckUniversalCompanyBranch.setSelected(universal);

            jckAllEntity.setEnabled(universal);
            jckAllEntity.setSelected(false);

            jlEntity.setEnabled(true);
            jltEntity.setEnabled(true);

            if (universal) {
                items = miClient.getSessionXXX().getAllCompanyBranchEntitiesAsComponentItems(moFieldCompanyBranchId.getKeyAsIntArray()[0], mnEntityCategoryId, false);
            }
            else {
                items = miClient.getSessionXXX().getUserCompanyBranchEntitiesAsComponentItems(moFieldCompanyBranchId.getKeyAsIntArray()[0], mnEntityCategoryId, false);
            }

            jltEntity.setListData(items);
        }
    }

    public void actionOk() {
        if (!jckAllCompanyBranch.isSelected() && jcbCompanyBranch.getSelectedIndex() <= 0) {
            miClient.showMsgBoxWarning(SLibConstants.MSG_ERR_GUI_FIELD_EMPTY + "'" + jlCompanyBranch.getText() + "'.");
            jcbCompanyBranch.requestFocus();
        }
        else if (!jckAllCompanyBranch.isSelected() && !jckAllEntity.isSelected() && jltEntity.getSelectedIndex() == -1) {
            miClient.showMsgBoxWarning(SLibConstants.MSG_ERR_GUI_FIELD_EMPTY + "'" + jlEntity.getText() + "'.");
            jltEntity.requestFocus();
        }
        else {
            mnCompanyBranchId = jckAllCompanyBranch.isSelected() ? SLibConstants.UNDEFINED : moFieldCompanyBranchId.getKeyAsIntArray()[0];
            mnEntityId = jckAllCompanyBranch.isSelected() || jckAllEntity.isSelected() ? SLibConstants.UNDEFINED : moFieldEntity.getKeyAsIntArray()[1];

            mnFormResult = SLibConstants.FORM_RESULT_OK;
            setVisible(false);
        }
    }

    public void actionCancel() {
        mnFormResult = SLibConstants.FORM_RESULT_CANCEL;
        setVisible(false);
    }

    public void actionSelectSession() {
        int[] key = miClient.getSessionXXX().getCurrentCompanyBranchEntityKey(mnEntityCategoryId);

        if (key == null) {
            if (miClient.getSessionXXX().getCurrentCompanyBranchId() != SLibConstants.UNDEFINED) {
                key = new int[] { miClient.getSessionXXX().getCurrentCompanyBranchId(), SLibConstants.UNDEFINED };
            }
        }

        setCompanyBranchEntityKey(key);
    }

