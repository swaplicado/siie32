    private void windowActivated() {
        if (mbFirstTime) {
            mbFirstTime = false;
            moPanelBizPartnerBranchAddress.requestFocusFirstTime();
        }
    }

    private void actionOk() {
        erp.lib.form.SFormValidation validationBranch = moPanelBizPartnerBranchAddress.formValidate();

        if (validationBranch.getIsError()) {
            if (validationBranch.getComponent() != null) {
                validationBranch.getComponent().requestFocus();
            }
            if (validationBranch.getMessage().length() > 0) {
                miClient.showMsgBoxWarning(validationBranch.getMessage());
            }
        }
        else {
            mnFormResult = SLibConstants.FORM_RESULT_OK;
            setVisible(false);
        }
    }

    private void actionCancel() {
        mnFormResult = SLibConstants.FORM_RESULT_CANCEL;
        setVisible(false);
    }

