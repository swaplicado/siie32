    public void resetForm() {
        mnFormResult = SLibConstants.UNDEFINED;
        mbFirstTime = true;

        manPayrollKey = null;
        msPayrollPeriod = "";
        msPayrollNumber = "";
        msPayrollDates = "";
        msPayrollNotes = "";
        mdPayrollNet = 0;

        populate();
    }

