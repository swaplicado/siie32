    private void initComponentsCustom() {
        miClient.getSession().populateCatalogue(moKeyStatusReq, SModConsts.TRNS_ST_MAT_REQ, 0, new SGuiParams(SDbRegistry.FIELD_CODE));
        
        bgStatus.clearSelection();
        actionClearStatusReq();
        
        jbClearStatusReq.addActionListener(this);
        moKeyStatusReq.addItemListener(this);
    }
    
    
    
    private void actionClearStatusReq() {
        jbClearStatusReq.setEnabled(false);
        
        if (moKeyStatusReq.getSelectedIndex() > 0) {
            moKeyStatusReq.setSelectedIndex(0); // triggers an item state changed event
            moKeyStatusReq.requestFocusInWindow();
        }
    }
    
    private void itemStateChangedStatusReq() {
        moPaneView.putFilter(SModConsts.TRNS_ST_MAT_REQ, new SGridFilterValue(SModConsts.TRNS_ST_MAT_REQ, SGridConsts.FILTER_DATA_TYPE_INT_ARRAY, ((SGuiItem) moKeyStatusReq.getSelectedItem()).getPrimaryKey()));
        jbClearStatusReq.setEnabled(moKeyStatusReq.getSelectedIndex() > 0);
    }
    
