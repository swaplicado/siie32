    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() instanceof JButton) {
            JButton button = (JButton) e.getSource();
            
            if (button == jbSelect) {
                actionSelect();
            }
            else if (button == jbClose) {
                actionClose();
            }
        }
    }
