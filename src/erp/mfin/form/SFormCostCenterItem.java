    @Override
    public void setRegistry(erp.lib.data.SDataRegistry registry) {
        mbResetingForm = true;

        moDataCostCenterItem = (SDataCostCenterItem) registry;

        moFieldPkLinkTypeId.setFieldValue(new int[] { moDataCostCenterItem.getPkLinkTypeId() });
        itemStateChangedPkLinkTypeId();

        switch (moFieldPkLinkTypeId.getKeyAsIntArray()[0]) {
            case SDataConstantsSys.TRNS_TP_LINK_ALL:
                moFieldPkReferenceId.setFieldValue(new int[] { SLibConstants.UNDEFINED });  // Just for consistency, but not needed
                break;
            case SDataConstantsSys.TRNS_TP_LINK_CT_ITEM:
            case SDataConstantsSys.TRNS_TP_LINK_CL_ITEM:
            case SDataConstantsSys.TRNS_TP_LINK_TP_ITEM:
                SFormUtilities.locateComboBoxItemByComplement(jcbPkReferenceId, moDataCostCenterItem.getPkReferenceId());
                break;
            default:
                moFieldPkReferenceId.setFieldValue(new int[] { moDataCostCenterItem.getPkReferenceId() });
        }

        moCostCentertPanel.setSelectedAccount(new SAccount(moDataCostCenterItem.getFkCostCenterId(), (String) miClient.getSession().readField(SModConsts.FIN_CC, new int[] { moDataCostCenterItem.getFkCostCenterId() }, SDbRegistry.FIELD_CODE), "", false, SLibConstants.UNDEFINED, SLibConstants.UNDEFINED));

        moFieldIsDeleted.setFieldValue(moDataCostCenterItem.getIsDeleted());

        if (!moDataCostCenterItem.getIsRegistryNew()) {
            jcbPkLinkTypeId.setEnabled(false);
            jbPkLinkTypeId.setEnabled(false);
            jcbPkReferenceId.setEnabled(false);
            jbPkReferenceId.setEnabled(false);
        }

        jckIsDeleted.setEnabled(!moDataCostCenterItem.getIsRegistryNew());

        mbResetingForm = false;
    }

    @Override
    public erp.lib.data.SDataRegistry getRegistry() {
        if (moDataCostCenterItem == null) {
            moDataCostCenterItem = new SDataCostCenterItem();
            moDataCostCenterItem.setPkLinkTypeId(moFieldPkLinkTypeId.getKeyAsIntArray()[0]);

            switch (moFieldPkLinkTypeId.getKeyAsIntArray()[0]) {
                case SDataConstantsSys.TRNS_TP_LINK_ALL:
                    moDataCostCenterItem.setPkReferenceId(SLibConstants.UNDEFINED);
                    break;
                case SDataConstantsSys.TRNS_TP_LINK_CT_ITEM:
                case SDataConstantsSys.TRNS_TP_LINK_CL_ITEM:
                case SDataConstantsSys.TRNS_TP_LINK_TP_ITEM:
                    moDataCostCenterItem.setPkReferenceId((Integer) ((SFormComponentItem) jcbPkReferenceId.getSelectedItem()).getComplement());
                    break;
                default:
                    moDataCostCenterItem.setPkReferenceId(moFieldPkReferenceId.getKeyAsIntArray()[0]);
            }

            moDataCostCenterItem.setFkUserNewId(miClient.getSession().getUser().getPkUserId());
        }
        else {
            moDataCostCenterItem.setFkUserEditId(miClient.getSession().getUser().getPkUserId());
        }

        moDataCostCenterItem.setIsDeleted(moFieldIsDeleted.getBoolean());
        moDataCostCenterItem.setFkCostCenterIdXXX(SAccountUtils.convertCodeUsr(((SDataParamsCompany) miClient.getSession().getConfigCompany()).getMaskCostCenter(), moCostCentertPanel.getSelectedAccount().getCodeStd()));
        moDataCostCenterItem.setFkCostCenterId(moCostCentertPanel.getSelectedAccount().getAccountId());

        return moDataCostCenterItem;
    }

