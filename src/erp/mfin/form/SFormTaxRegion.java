    @Override
    public void setRegistry(erp.lib.data.SDataRegistry registry) {
        moTaxRegion = (SDataTaxRegion) registry;

        moFieldTaxRegion.setFieldValue(moTaxRegion.getTaxRegion());
        moFieldIsDeleted.setFieldValue(moTaxRegion.getIsDeleted());

        jckIsDeleted.setEnabled(true);
    }

    @Override
    public erp.lib.data.SDataRegistry getRegistry() {
        if (moTaxRegion == null) {
            moTaxRegion = new SDataTaxRegion();
            moTaxRegion.setFkUserNewId(miClient.getSession().getUser().getPkUserId());
        }
        else {
            moTaxRegion.setFkUserEditId(miClient.getSession().getUser().getPkUserId());
        }

        moTaxRegion.setTaxRegion(moFieldTaxRegion.getString());
        moTaxRegion.setIsDeleted(moFieldIsDeleted.getBoolean());

        return moTaxRegion;
    }

