    @Override
    public erp.lib.form.SFormValidation formValidate() {
        SFormValidation validation = new SFormValidation();

        for (int i = 0; i < mvFields.size(); i++) {
            if (!((erp.lib.form.SFormField) mvFields.get(i)).validateField()) {
                validation.setIsError(true);
                validation.setComponent(((erp.lib.form.SFormField) mvFields.get(i)).getComponent());
                break;
            }
        }

        if (!validation.getIsError()) {
            Object[] oParamsIn = new Object[] { moTaxRegion == null ? 0 : moTaxRegion.getPkTaxRegionId(), moFieldTaxRegion.getString() };

            if ( SDataUtilities.callProcedureVal(miClient, SProcConstants.FINU_TAX_REG_VAL, oParamsIn, SLibConstants.EXEC_MODE_VERBOSE) > 0 ) {
                validation.setMessage("La región de impuestos ya existe.");
                validation.setComponent(jtfTaxRegion);
            }
        }

        return validation;
    }

