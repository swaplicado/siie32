    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SDbMaterialConsumptionEntity) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
        }
        else {
            jtfRegistryKey.setText(SLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        moTextCode.setValue(moRegistry.getCode());
        moTextConsumptionEntity.setValue(moRegistry.getName());
        
        
        setFormEditable(true);

        if (moRegistry.isRegistryNew()) { }

        addAllListeners();
    }

    @Override
    public SDbRegistry getRegistry() throws Exception {
        SDbMaterialConsumptionEntity registry = (SDbMaterialConsumptionEntity) moRegistry.clone();

        if (registry.isRegistryNew()) {}
        
        moRegistry.setCode(moTextCode.getValue());
        moRegistry.setName(moTextConsumptionEntity.getValue());
        
        return registry;
    }

