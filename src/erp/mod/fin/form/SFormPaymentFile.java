    private void setComponetsFileData(SDbPaymentFile file) {
        if (file != null) {
            switch (file.getPaymentFileType()) {
                case SModSysConsts.FIN_PAY_FILE_TP_EF:
                    moRadPhoto.setSelected(true);
                    break;
                case SModSysConsts.FIN_PAY_FILE_TP_RA:
                    moRadAdvance.setSelected(true);
                    break;
                case SModSysConsts.FIN_PAY_FILE_TP_FS:
                    moRadService.setSelected(true);
                    break;
            }
            jtfFile.setText(file.getFileName());
            moTextDescription.setValue(file.getFileDescription());
        }
        else {
            clearFileControls();
        }
        
        mbIsCapturingFile = false;
        enableFileControls(false);
    }
    
    private SGuiValidation validateFile() {
        SGuiValidation validation = moFieldsEty.validateFields();
        
        if (validation.isValid() && (moFile == null || jtfFile.getText().isEmpty())) {
            validation.setMessage("No se ha seleccionado un archivo.");
            validation.setComponent(jtfFile);
        }
        if (validation.isValid() && !moFile.exists()) {
            validation.setMessage("El archivo de soporte no existe.");
            validation.setComponent(jtfFile);
        }
        
        return validation;
    }
    
    private void actionEtyNew() {
        if (jbEtyNew.isEnabled()) {
            mbIsCapturingFile = true;
            clearFileControls();
            enableFileControls(true);
        }
    }
    
    private void actionEtyDelete() {
        if (jbEtyDelete.isEnabled()) {
            if (moGridFiles.getSelectedGridRow() == null) {
                miClient.showMsgBoxInformation("Debe haber un renglón seleccionado.");
            }
            else {
                int index = moGridFiles.getTable().getSelectedRow();
                maPaymentFileDeleted.add(maPaymentFiles.get(index));
                maPaymentFiles.remove(index);
                populateGridFiles();
                moGridFiles.setSelectedGridRow(index);
                clearFileControls();
                valueChanged(null);
            }
        }
    }

    private void actionEtyAdd() {
        if (jbEtyAdd.isEnabled()) {
            SGuiValidation validation = validateFile();
            if (validation.isValid()) {
                enableFileControls(false);
                SDbPaymentFile file = new SDbPaymentFile();
                file.setPaymentFileType(moRadPhoto.isSelected() ? SModSysConsts.FIN_PAY_FILE_TP_EF : moRadAdvance.isSelected() ? SModSysConsts.FIN_PAY_FILE_TP_RA : SModSysConsts.FIN_PAY_FILE_TP_FS);
                file.setAuxFile(moFile);
                file.setFileName(moFile.getName());
                file.setFileDescription(moTextDescription.getValue());
                file.setSortingPos(maPaymentFiles.size() + 1);
                    
                maPaymentFiles.add(file);
                
                clearFileControls();
                populateGridFiles();
                
                moGridFiles.setSelectedGridRow(moGridFiles.getModel().getRowCount() - 1);
                
                mbIsCapturingFile = false;
                
                valueChanged(null);
            }
            else {
                miClient.showMsgBoxInformation(validation.getMessage());
                validation.getComponent().requestFocus();
            }
        }
    }

    private void actionEtyCancel() {
        if (jbEtyCancel.isEnabled()) {
            clearFileControls();
            enableFileControls(false);
            mbIsCapturingFile = false;
            populateGridFiles();
            valueChanged(null);
        }
    }

    private void actionFilePicker() {
        if (jbFilePicker.isEnabled()) {
            miClient.getFileChooser().setSelectedFile(new File(System.getProperty("user.home")));
            if (miClient.getFileChooser().showOpenDialog(miClient.getFrame()) == JFileChooser.APPROVE_OPTION) {
                if ( miClient.getFileChooser().getSelectedFile().getName().toLowerCase().matches(".*\\.(" + FILE_EXT + ")$")) {
                    jtfFile.setText(miClient.getFileChooser().getSelectedFile().getAbsolutePath());
                    jtfFile.setToolTipText(miClient.getFileChooser().getSelectedFile().getAbsolutePath());
                    moFile = miClient.getFileChooser().getSelectedFile();
                }
                else {
                    miClient.showMsgBoxError("Formato de archivo no compatible.");
                }
            }
        }
    }
    
    private void actionUpRow() {
        if (jbUp.isEnabled()) {
            if (moGridFiles.getSelectedGridRow() == null) {
                miClient.showMsgBoxInformation("Debe haber un renglón seleccionado.");
            }
            else {
                int index = moGridFiles.getTable().getSelectedRow();
                if (index != 0) {
                    SDbPaymentFile row = maPaymentFiles.remove(index);
                    maPaymentFiles.add(index - 1, row);
                    int i = 1;
                    for (SDbPaymentFile rowAux : maPaymentFiles) {
                        rowAux.setSortingPos(i++);
                    }
                    populateGridFiles();
                    moGridFiles.setSelectedGridRow(index - 1);
                }
            }
        }
    }

    private void actionDownRow() {
        if (jbDown.isEnabled()) {
            if (moGridFiles.getSelectedGridRow() == null) {
                miClient.showMsgBoxInformation("Debe haber un renglón seleccionado.");
            }
            else {
                int index = moGridFiles.getTable().getSelectedRow();
                if (index < maPaymentFiles.size() - 1) {
                    SDbPaymentFile row = maPaymentFiles.remove(index);
                    maPaymentFiles.add(index + 1, row);
                    int i = 1;
                    for (SDbPaymentFile rowAux : maPaymentFiles) {
                        rowAux.setSortingPos(i++);
                    }
                    populateGridFiles();
                    moGridFiles.setSelectedGridRow(index + 1);
                }
            }
        }
    }

