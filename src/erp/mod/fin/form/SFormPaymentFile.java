    @Override
    public void reloadCatalogues() {
        miClient.getSession().populateCatalogue(moKeyBeneficiary, SModConsts.BPSU_BP, SLibConsts.UNDEFINED, null);
        if (!isApplingFunctionalAreas()) {
            miClient.getSession().populateCatalogue(moKeyFunctionalArea, SModConsts.CFGU_FUNC_SUB, SLibConsts.UNDEFINED, null);
        }
        else {
            miClient.getSession().populateCatalogue(moKeyFunctionalArea, SModConsts.CFGU_FUNC_SUB, miClient.getSession().getUser().getPkUserId(), null);
        }
    }

    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SDbPayment) registry;
        
        mbCanCapture = false;
        mbIsCapturingFile = false;
        mbSendAuth = false;
        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;
        
        removeAllListeners();
        reloadCatalogues();
        
        if (moRegistry.isRegistryNew()) { }
        
        jtfRegistryKey.setText(moRegistry.getPkPaymentId() + "");
        moDateApplication.setValue(moRegistry.getDateApplication());
        moIntNumber.setValue(moRegistry.getNumber());
        moKeyBeneficiary.setValue(new int[] { moRegistry.getFkBeneficiaryId() });
        moTextSerie.setValue(moRegistry.getSeries());
        moDecPaymentCurrency.setValue(moRegistry.getPaymentCurrency());
        moTextPaymentCurrencyCy.setValue(moRegistry.getCurrency().getKey());
        moDateRequired.setValue(moRegistry.getDateRequired());
        moKeyFunctionalArea.setValue(new int[] { moRegistry.getFkFunctionalAreaId(), moRegistry.getFkFunctionalSubareaId() });
        
        maPaymentFiles = new ArrayList<>();
        for (SDbPaymentFile file : moRegistry.getFiles()) {
            maPaymentFiles.add(file);
        }
        
        maPaymentFileDeleted = new ArrayList<>();
        
        populateGridFiles();
        
        if (moRegistry.getFkStatusPaymentId() == SModSysConsts.FINS_ST_PAY_NEW) { 
            mbCanCapture = true;
            enablePayComponets(true);
        }
        else {
            mbCanCapture = false;
            enablePayComponets(false);
        }
        
        clearFileControls();
        enableFileControls(false);
        valueChanged(null);
        addAllListeners();
    }

    @Override
    public SDbRegistry getRegistry() throws Exception {
        SDbPayment registry = (SDbPayment) moRegistry.clone();
        
        registry.getFiles().clear();
        for (SDbPaymentFile file : maPaymentFiles) {
            registry.getFiles().add(file);
        }
        registry.setAuxReloadEntries(false);
        
        registry.getFilesDeleted().clear();
        for (SDbPaymentFile file : maPaymentFileDeleted) {
            registry.getFilesDeleted().add(file);
        }
        
        if (mbSendAuth) {
            
        }
        
        return registry;
    }

    @Override
    public SGuiValidation validateForm() {
        SGuiValidation validation = moFields.validateFields();
        
        if (validation.isValid()) {
            if (moGridFiles.getModel().getRowCount() <= 0) {
                validation.setMessage("Debe añadir al menos un archivo");
                validation.setComponent(jbEtyNew);
            }
        }
        
        return validation;
    }

    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() instanceof JButton) {
            JButton button = (JButton) e.getSource();
            
            if (button == jbEtyNew) {
                actionEtyNew();
            }
            else if (button == jbEtyDelete) {
                actionEtyDelete();
            }
            else if (button == jbEtyAdd) {
                actionEtyAdd();
            }
            else if (button == jbEtyCancel) {
                actionEtyCancel();
            }
            else if (button == jbFilePicker) {
                actionFilePicker();
            }
            else if (button == jbUp) {
                actionUpRow();
            }
            else if (button == jbDown) {
                actionDownRow();
            }
            else if (button == jbSaveAndSend) {
                actionSaveAndSend();
            }
        }
    }

    @Override
    public void valueChanged(ListSelectionEvent e) {
        SDbPaymentFile selectedRow = (SDbPaymentFile) moGridFiles.getSelectedGridRow();
        if (selectedRow != null) {
            if (!jbEtyAdd.isEnabled()) {
                setComponetsFileData(selectedRow);
            }
        }
    }
