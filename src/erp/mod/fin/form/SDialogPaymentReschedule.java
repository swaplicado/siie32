    @Override
    public Object getValue(final int type) {
        Object value = null;
        
        switch (type) {
            case VALUE_DATE:
                value = moDateNewDate.getValue();
                break;
                
            case VALUE_NOTES:
                value = moTextNotes.getValue();
                break;
                
            case VALUE_NOTES_AUTH:
                value = moTextNotesAuthorization.getValue();
                break;
                
            default:
                // nothing
        }
        
        return value;
    }
