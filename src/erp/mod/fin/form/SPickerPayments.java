    private void readPayments(String where) {
        try {
            String entryTp;
            if (mnFormSubtype == SModSysConsts.FINX_LAY_BANK_TRN_TP_PAY) {
                entryTp = SDbPaymentEntry.TYPE_PAYMENT;
                where += "AND EXISTS (SELECT * FROM erp.bpsu_bank_acc AS ac WHERE bpb.id_bpb = ac.id_bpb AND ac.fid_bank " + 
                    (SLibUtils.belongsTo(mnBankPaymentTypeId, new int[] { SDataConstantsSys.FINS_TP_PAY_BANK_THIRD, SDataConstantsSys.FINS_TP_PAY_BANK_AGREE }) ? "= " : "<> ") + mnBizPartnerBankId + ") ";
            }
            else {
                entryTp = SDbPaymentEntry.TYPE_ADVANCE;
            }
            Statement statement = miClient.getSession().getDatabase().getConnection().createStatement();
            String sql = "SELECT d.id_year, d.id_doc, p.id_pay, " +
                    "b.bp, " +
                    "IF(p.ser <> '', CONCAT(p.ser, '-', p.num), p.num) AS folio_p, " +
                    "IF(d.num_ser <> '', CONCAT(d.num_ser, '-', d.num), d.num) AS folio_d, " +
                    "pe.des_pay_app_ety_cur, " +
                    "c.cur_key, " +
                    "p.pay_app_cur, " +
                    "cp.cur_key, " +
                    "p.pay_exc_rate_app, " +
                    "b_rcpt_pay_req, " +
                    "p.fk_func, p.fk_func_sub, " +
                    "p.fk_ben, " +
                    "p.nts, " +
                    "p.dt_sched_n, " +
                    "pe.install, " +
                    "pe.doc_bal_prev_app_cur, " +
                    "pe.doc_bal_unpd_app_cur_r, " +
                    "pe.doc_bal_prev_cur, " +
                    "pe.doc_bal_unpd_cur_r " +
                    "FROM fin_pay AS p " +
                    "INNER JOIN fin_pay_ety AS pe ON p.id_pay = pe.id_pay " +
                    "INNER JOIN erp.bpsu_bp AS b ON p.fk_ben = b.id_bp " +
                    "INNER JOIN erp.cfgu_cur AS c ON pe.fk_ety_cur = c.id_cur " +
                    "INNER JOIN erp.cfgu_cur AS cp ON p.fk_cur = cp.id_cur " +
                    "LEFT JOIN trn_dps AS d ON pe.fk_doc_year_n = d.id_year AND pe.fk_doc_doc_n = d.id_doc " +
                    "LEFT JOIN erp.bpsu_bpb AS bpb ON d.fid_bpb = bpb.id_bpb " +
                    "WHERE NOT p.b_del AND p.fk_st_pay = " + SModSysConsts.FINS_ST_PAY_SCHED + " " +
                    "AND p.fk_cur = " + mnCurrrencyLayout + " " + 
                    "AND pe.fk_ety_cur = " + mnCurPaymentDoc + " " + 
                    "AND pe.ety_tp = '" + entryTp + "' " +
                    where + " " + 
                    "ORDER BY b.bp, p.dt_sched_n, d.num_ser, d.num, p.ser, p.num";
            try (ResultSet resultSet = statement.executeQuery(sql)) {
                while (resultSet.next()) {
                    SRowPayments row = new SRowPayments();
                    row.setIdYear(resultSet.getInt("d.id_year"));
                    row.setIdDoc(resultSet.getInt("d.id_doc"));
                    row.setIdPayment(resultSet.getInt("p.id_pay"));
                    row.setBeneficiary(resultSet.getString("b.bp"));
                    row.setPayNum(resultSet.getString("folio_p"));
                    row.setDocNum(resultSet.getString("folio_d"));
                    row.setAmount(resultSet.getDouble("des_pay_app_ety_cur"));
                    row.setCur(resultSet.getString("c.cur_key"));
                    row.setExchangeRate(resultSet.getDouble("p.pay_exc_rate_app"));
                    row.setAmountCurrencyToPay(resultSet.getDouble("p.pay_app_cur"));
                    row.setCurToPay(resultSet.getString("cp.cur_key"));
                    row.setReceptionPayReq(resultSet.getBoolean("b_rcpt_pay_req"));
                    row.setFuncArea(resultSet.getInt("p.fk_func"));
                    row.setFuncSubarea(resultSet.getInt("p.fk_func_sub"));
                    row.setIdBeneficiary(resultSet.getInt("p.fk_ben"));
                    row.setNotes(resultSet.getString("p.nts"));
                    row.setDateScheduled(resultSet.getDate("p.dt_sched_n"));
                    row.setInstallment(resultSet.getInt("pe.install"));
                    row.setDocBalancePrevAppCy(resultSet.getInt("pe.doc_bal_prev_app_cur"));
                    row.setDocBalanceUnpayAppCy(resultSet.getInt("pe.doc_bal_unpd_app_cur_r"));
                    row.setDocBalancePrevCy(resultSet.getInt("pe.doc_bal_prev_cur"));
                    row.setDocBalanceUnpayCy(resultSet.getInt("pe.doc_bal_unpd_cur_r"));
                    row.setSelected(false);
                    boolean found = false;
                    for (SRowPayments selectedRow : maAllSelectedPayments) {
                        if (mnFormSubtype == SModSysConsts.FINX_LAY_BANK_TRN_TP_PAY) {
                            if (SLibUtils.compareKeys(row.getRowPrimaryKey(), selectedRow.getRowPrimaryKey())) {
                                found = true;
                                break;
                            }
                        }
                        else {
                            if (row.getIdPayment() == row.getIdPayment()) {
                                found = true;
                                break;
                            }
                        }
                    }
                    if (!found) {
                        maPayments.add(row);
                    }
                }
            }
        }
        catch (Exception e) {
            miClient.showMsgBoxError(e.getMessage());
        }
    }
    
    private void populateGrid() {
        Vector<SGridRow> vRows = new Vector<>();
        vRows.addAll(maPayments);
        moGridPayments.populateGrid(vRows);
    }
    
    private void actionLayoutDate() {
        readPayments("AND p.dt_sched_n = '" + SLibUtils.DbmsDateFormatDate.format(mtDueDate) + "' ");
        jbLayoutDate.setEnabled(false);
        jbLayoutDateUntil.setEnabled(false);
        jbLayoutDateAll.setEnabled(false);
        jbResearch.setEnabled(true);
        jlText.setText("Fecha layout: " + SLibUtils.DateFormatDate.format(mtDueDate));
        populateGrid();
        
    }

    private void actionLayoutDateUntill() {
        readPayments("AND p.dt_sched_n <= '" + SLibUtils.DbmsDateFormatDate.format(mtDueDate) + "' ");
        jbLayoutDate.setEnabled(false);
        jbLayoutDateUntil.setEnabled(false);
        jbLayoutDateAll.setEnabled(false);
        jbResearch.setEnabled(true);
        jlText.setText("Fecha layout hasta: " + SLibUtils.DateFormatDate.format(mtDueDate));
        populateGrid();
    }

    private void actionLayoutDateAll() {
        readPayments("");
        jbLayoutDate.setEnabled(false);
        jbLayoutDateUntil.setEnabled(false);
        jbLayoutDateAll.setEnabled(false);
        jbResearch.setEnabled(true);
        jlText.setText("Todos los pagos");
        populateGrid();
    }

    private void actionResearch() {
        jbLayoutDate.setEnabled(true);
        jbLayoutDateUntil.setEnabled(true);
        jbLayoutDateAll.setEnabled(true);
        jbResearch.setEnabled(false);
        maPayments.clear();
        jlText.setText("");
        populateGrid();
    }
    
    public ArrayList<SRowPayments> getSelectedPayments() {
        ArrayList<SRowPayments> selectedPayments = new ArrayList<>();
        
        for (SGridRow row : moGridPayments.getModel().getGridRows()) {
            SRowPayments pay = (SRowPayments) row;
            if (pay.getIsSelected()) {
                maPayments.remove(pay);
                maAllSelectedPayments.add(pay);
                selectedPayments.add(pay);
            }
        }
        
        return selectedPayments;
    }
    
    public void addPayments(ArrayList<SRowPayments> pays) {
        maPayments.addAll(pays);
        maAllSelectedPayments.removeAll(pays);
    }
    
