    @Override
    public void actionCancel() {
        try {
            if (moPDDocument != null) {
                moPDDocument.close();
            }
            
            if (jchkDeleteOnClose.isSelected()) {
                Files.delete(moPdf.toPath());
            }
        }
        catch (IOException ioe) {
            SLibUtils.showException(this, ioe);
        }
        finally {
            resetForm();

            mnFormResult = SGuiConsts.FORM_RESULT_CANCEL;
            setVisible(false);
        }
    }

    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() instanceof JButton) {
            JButton button = (JButton) e.getSource();
            
            if (button == jbGoFirstPage) {
                actionPerformedGoFirstPage();
            }
            else if (button == jbGoPrevPage) {
                actionPerformedGoPrevPage();
            }
            else if (button == jbGoNextPage) {
                actionPerformedGoNextPage();
            }
            else if (button == jbGoLastPage) {
                actionPerformedGoLastPage();
            }
            else if (button == jbZoomIn) {
                actionPerformedZoomIn();
            }
            else if (button == jbZoomReset) {
                actionPerformedZoomReset();
            }
            else if (button == jbZoomOut) {
                actionPerformedZoomOut();
            }
            else if (button == jbFileSave) {
                actionPerformedFileSave();
            }
            else if (button == jbFileInfo) {
                actionPerformedFileInfo();
            }
            else if (button == jbFileDeleteToUpdate) {
                actionPerformedFileDeleteToUpdate();
            }
        }
    }
