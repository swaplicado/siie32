    private void showPage() {
        if (moPdf == null) {
            jtfCurrentPage.setText("");
        }
        else {
            jtfCurrentPage.setText((mnCurrentPageIndex + 1) + "/" + mnPages);
        }
    }
    
    private void showZoom() {
        if (moPdf == null) {
            jtfCurrentZoom.setText("");
        }
        else {
            jtfCurrentZoom.setText(ZOOM_OPTIONS[mnCurrentZoomIndex] + "%");
        }
    }
    
    private void showFile() {
        if (moPdf == null) {
            jtfDocument.setText("");
            jtfDocument.setToolTipText(TOOL_TIP_DOCUMENT);
            
            jtfFile.setText("");
            jtfFile.setToolTipText(TOOL_TIP_FILE);
        }
        else {
            jtfDocument.setText(moDocument.getEffectiveFolio());
            jtfDocument.setToolTipText(TOOL_TIP_DOCUMENT + ": " + jtfDocument.getText());
            
            jtfFile.setText(moPdf.getName());
            jtfFile.setToolTipText(TOOL_TIP_FILE + ": " + jtfFile.getText());
        }
    }
    
    private void renderCurrentPage() {
        try {
            SGuiUtils.setCursorWait(miClient);
            // prepare page image:

            String key = mnCurrentPageIndex + "-" + mnCurrentZoomIndex;
            BufferedImage pageImage = moPageImages.get(key);

            if (pageImage == null) {
                pageImage = moPDFRenderer.renderImage(mnCurrentPageIndex, ZOOMS[mnCurrentZoomIndex]);
                moPageImages.put(key, pageImage);
            }

            // show page:

            moLabel = new JLabel(new ImageIcon(pageImage));
            jScrollPane.getViewport().setView(moLabel);
        }
        catch (IOException ioe) {
            SLibUtils.showException(this, ioe);
        }
        finally {
            SGuiUtils.setCursorDefault(miClient);
        }
    }
    
    private void actionPerformedGoFirstPage() {
        if (mnCurrentPageIndex != 0) {
            mnCurrentPageIndex = 0;
            renderCurrentPage();
            showPage();
        }
    }
    
    private void actionPerformedGoPrevPage() {
        if (mnCurrentPageIndex > 0) {
            mnCurrentPageIndex--;
            renderCurrentPage();
            showPage();
        }
    }

    private void actionPerformedGoNextPage() {
        if (mnCurrentPageIndex < (mnPages - 1)) {
            mnCurrentPageIndex++;
            renderCurrentPage();
            showPage();
        }
    }

    private void actionPerformedGoLastPage() {
        if (mnCurrentPageIndex != (mnPages - 1)) {
            mnCurrentPageIndex = mnPages - 1;
            renderCurrentPage();
            showPage();
        }
    }

    private void actionPerformedZoomIn() {
        if (mnCurrentZoomIndex > 0) {
            mnCurrentZoomIndex--;
            renderCurrentPage();
            showZoom();
        }
    }

    private void actionPerformedZoomReset() {
        if (mnCurrentZoomIndex != ZOOM_INDEX_100) {
            mnCurrentZoomIndex = ZOOM_INDEX_100;
            renderCurrentPage();
            showZoom();
        }
    }

    private void actionPerformedZoomOut() {
        if (mnCurrentZoomIndex < (ZOOMS.length - 1)) {
            mnCurrentZoomIndex++;
            renderCurrentPage();
            showZoom();
        }
    }

    private void actionPerformedFileSave() {
        try {
            FileFilter filter = SFileUtilities.createFileNameExtensionFilter(SFileUtilities.pdf);
            JFileChooser fileChooser = miClient.getFileChooser();
            fileChooser.repaint();
            fileChooser.setAcceptAllFileFilterUsed(false);
            fileChooser.setFileFilter(filter);

            String safeFileName = moDocument.getEffectiveFolio().replaceAll("[\\\\/:*?\"<>|]", "");
            fileChooser.setSelectedFile(new File(safeFileName + "." + SFileUtilities.pdf));

            if (fileChooser.showSaveDialog(miClient.getFrame()) == JFileChooser.APPROVE_OPTION) {
                File newFile = fileChooser.getSelectedFile();

                // Ensure file ends with ".zip"
                if (!newFile.getName().toLowerCase().endsWith("." + SFileUtilities.pdf)) {
                    newFile = new File(newFile.getAbsolutePath() + "." + SFileUtilities.pdf);
                }
                
                Files.copy(
                    moPdf.toPath(),
                    newFile.toPath(),
                    StandardCopyOption.REPLACE_EXISTING
                );
                
                miClient.showMsgBoxInformation("El siguiente archivo ha sido guardado:\n" + newFile.getAbsolutePath());
            }
        }
        catch (IOException ioe) {
            SLibUtils.showException(this, ioe);
        }
        finally {
            JFileChooser fileChooser = miClient.getFileChooser();
            fileChooser.resetChoosableFileFilters();
            fileChooser.setAcceptAllFileFilterUsed(true);
        }
    }

    private void actionPerformedFileInfo() {
        try {
            BasicFileAttributes attrs = Files.readAttributes(moPdf.toPath(), BasicFileAttributes.class);
            
            String fileInfo = "Información del archivo PDF:\n\n"
                    + "Emisor documento: " + moDocument.getIssuer() + "\n"
                    + "Folio documento: " + moDocument.getEffectiveFolio() + "\n\n"
                    + "Archivo: " + moPdf.getAbsolutePath() + "\n"
                    + "Creado: " + SLibUtils.DateFormatDatetimeTimeZone.format(new Date(attrs.creationTime().toMillis())) + "\n"
                    + "Modificado: " + SLibUtils.DateFormatDatetimeTimeZone.format(new Date(attrs.lastModifiedTime().toMillis())) + "\n"
                    + "Último acceso: " + SLibUtils.DateFormatDatetimeTimeZone.format(new Date(attrs.lastAccessTime().toMillis()));
            
            miClient.showMsgBoxInformation(fileInfo);
        }
        catch (IOException ioe) {
            SLibUtils.showException(this, ioe);
        }
    }
    private void actionPerformedFileDeleteToUpdate() {
        if (!jchkDeleteOnClose.isSelected()) {
            String confirmDeletion = "¡Este archivo " + SFileUtilities.pdf.toUpperCase() + " del documento '" + moDocument.getEffectiveFolio() + "' será descartado!\n"
                    + "La próxima vez que solicite visualizarlo será descargado nuevamente desde " + SSwapConsts.SWAP_SERVICES + ".\n"
                    + SGuiConsts.MSG_CNF_CONT;
            
            if (miClient.showMsgBoxConfirm(confirmDeletion) == JOptionPane.YES_OPTION) {
                // set delete on close:
                jchkDeleteOnClose.setSelected(true);
                miClient.showMsgBoxWarning("El archivo " + SFileUtilities.pdf.toUpperCase() + " del documento '" + moDocument.getEffectiveFolio() + "' será descartado al cerrarse este diálogo.");
            }
        }
        else {
            // revert delete on close:
            jchkDeleteOnClose.setSelected(false);
            miClient.showMsgBoxInformation("El archivo " + SFileUtilities.pdf.toUpperCase() + " del documento '" + moDocument.getEffectiveFolio() + "' ya no será descartado al cerrarse este diálogo.");
        }
    }

    public void setPdf(final SDocument document, final File pdf) {
        resetForm();
        
        try {
            moDocument = document;
            moPdf = pdf;
            
            if (!moPdf.exists()) {
                miClient.showMsgBoxError("El archivo PDF no existe:\n" + moPdf.getAbsolutePath());
            }
            else {
                moPDDocument = PDDocument.load(moPdf);
                moPageImages = new HashMap<>();
                mnPages = moPDDocument.getNumberOfPages();
                
                if (mnPages < 1) {
                    miClient.showMsgBoxError("El archivo PDF no tiene páginas:\n" + moPdf.getAbsolutePath());
                }
                else {
                    moPDFRenderer = new PDFRenderer(moPDDocument);
                    mnCurrentPageIndex = 0;
                    mnCurrentZoomIndex = ZOOM_INDEX_100;

                    showPage();
                    showZoom();
                    showFile();
                    enableHandlers();
                    
                    renderCurrentPage();
                }
            }
        }
        catch (IOException ioe) {
            SLibUtils.showException(this, ioe);
        }
    }
    
    private void enableHandlers() {
        boolean enablePagination = mnPages > 1;
        jbGoFirstPage.setEnabled(enablePagination);
        jbGoPrevPage.setEnabled(enablePagination);
        jbGoNextPage.setEnabled(enablePagination);
        jbGoLastPage.setEnabled(enablePagination);
        
        boolean enableZoom = mnPages > 0 && mnCurrentPageIndex >= 0;
        jbZoomIn.setEnabled(enableZoom);
        jbZoomReset.setEnabled(enableZoom);
        jbZoomOut.setEnabled(enableZoom);
        
        boolean enableFileHandling = moPdf != null;
        jbFileSave.setEnabled(enableFileHandling);
        jbFileInfo.setEnabled(enableFileHandling);
        jbFileDeleteToUpdate.setEnabled(enableFileHandling && mbAllowDelettion);
    }
    
    @Override
    public void resetForm() {
        jScrollPane.getViewport().setView(null);
        
        moDocument = null;
        moPdf = null;
        moPDDocument = null;
        moPDFRenderer = null;
        moLabel = null;
        moPageImages = null;
        mnPages = 0;
        mnCurrentPageIndex = -1;
        mnCurrentZoomIndex = -1;
        
        jchkDeleteOnClose.setSelected(false);
        
        showPage();
        showZoom();
        showFile();
        enableHandlers();
    }
    
