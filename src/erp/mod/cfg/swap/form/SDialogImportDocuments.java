                                        int index = moDocumentsGrid.getTable().getSelectedRow();
                                        moDocumentsGrid.renderGridRows();
                                        moDocumentsGrid.setSelectedGridRow(index);

                                        if (document.isPaymentRequested()) {
                                            mbExportPaymentRequests = true;
                                        }
