    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SDbItemDescription) registry;
        
        moTextItemKey.setValue(msItemKey);
        moTextName.setValue(msItemName);
        
        jepExtItemDesc.setContentType("text/html");
        jepExtItemDesc.setEditable(false);
        jepExtItemDesc.setCaretPosition(0);
        
        if (moRegistry.getItemDescription() != null && !moRegistry.getItemDescription().isEmpty() && !moRegistry.getIsDeleted()) {
            String html = moRegistry.getItemDescription()
                .replaceAll("(?i)<head.*?>.*?</head>", "")
                .replaceAll("(?i)<style.*?>.*?</style>", "")
                .replaceAll("(?i)<html.*?>", "")
                .replaceAll("(?i)</html>", "")
                .replaceAll("(?i)<body.*?>", "")
                .replaceAll("(?i)</body>", "")
                .replaceAll("<p", "<p style='margin:0;'");
            
            jepExtItemDesc.setText("<html><body style='margin:0;'>" + html + "</body></html>"); 
        }
        else {
            jepExtItemDesc.setText("");
        }
    }
    
