    @Override
    public void reloadCatalogues() {
        miClient.getSession().populateCatalogue(moKeyWorkerType, SModConsts.HRSU_TP_WRK, SLibConsts.UNDEFINED, null);
    }

    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        int workerType = SLibConsts.UNDEFINED;
        moRegistry = (SDbWorkerTypeSalary) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        workerType = moRegistry.getPkWorkerTypeId();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
            moRegistry.setDateStart(miClient.getSession().getCurrentDate());
            jtfRegistryKey.setText("");
        }
        else {
            jtfRegistryKey.setText(SLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        moKeyWorkerType.setValue(new int[] { workerType });
        moDateDateStart.setValue(moRegistry.getDateStart());
        moCurSalary.getField().setValue(moRegistry.getSalary());

        setFormEditable(true);

        if (moRegistry.isRegistryNew()) {
            moKeyWorkerType.setEnabled(true);
        }
        else {
            moKeyWorkerType.setEnabled(false);
        }

        addAllListeners();
    }

    @Override
    public SDbRegistry getRegistry() throws Exception {
        SDbWorkerTypeSalary registry = moRegistry.clone();

        if (registry.isRegistryNew()) {
            registry.setPkWorkerTypeId(moKeyWorkerType.getValue()[0]);
        }

        registry.setDateStart(moDateDateStart.getValue());
        registry.setSalary(moCurSalary.getField().getValue());

        return registry;
    }

    @Override
    public SGuiValidation validateForm() {
        SGuiValidation validation = moFields.validateFields();

        if (validation.isValid()) {
            if (moKeyWorkerType.getValue()[0] == SModSysConsts.HRSU_TP_WRK_NA) {
                validation.setMessage(SGuiConsts.ERR_MSG_FIELD_DIF + "'" + SGuiUtils.getLabelName(jlWorkerType) + "'.");
                validation.setComponent(moKeyWorkerType);
            }
        }

        return validation;
    }
