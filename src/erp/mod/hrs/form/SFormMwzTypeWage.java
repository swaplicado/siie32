    @Override
    public void reloadCatalogues() {
        miClient.getSession().populateCatalogue(moKeyMwzType, SModConsts.HRSU_TP_MWZ, SLibConsts.UNDEFINED, null);
    }

    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        int mwzType = SLibConsts.UNDEFINED;
        moRegistry = (SDbMwzTypeWage) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        mwzType = moRegistry.getPkMwzTypeId();

        if (moRegistry.isRegistryNew()) {
            moRegistry.initPrimaryKey();
            moRegistry.setDateStart(miClient.getSession().getCurrentDate());
            jtfRegistryKey.setText("");
        }
        else {
            jtfRegistryKey.setText(SLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        moKeyMwzType.setValue(new int[] { mwzType });
        moDateDateStart.setValue(moRegistry.getDateStart());
        moCurWage.getField().setValue(moRegistry.getWage());

        setFormEditable(true);

        if (moRegistry.isRegistryNew()) {
            moKeyMwzType.setEnabled(true);
        }
        else {
            moKeyMwzType.setEnabled(false);
        }

        addAllListeners();
    }

    @Override
    public SDbRegistry getRegistry() throws Exception {
        SDbMwzTypeWage registry = moRegistry.clone();

        if (registry.isRegistryNew()) {
            registry.setPkMwzTypeId(moKeyMwzType.getValue()[0]);
        }

        registry.setDateStart(moDateDateStart.getValue());
        registry.setWage(moCurWage.getField().getValue());

        return registry;
    }

