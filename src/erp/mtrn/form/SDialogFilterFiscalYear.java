    private void windowActivated() {
        if (mbFirstTime) {
            mbFirstTime = false;
            jcbFiscalYear.requestFocus();
        }
    }

    private void actionOk() {
        erp.lib.form.SFormValidation validation = formValidate();

        if (validation.getIsError()) {
            if (validation.getComponent() != null) {
                validation.getComponent().requestFocus();
            }
            if (validation.getMessage().length() > 0) {
                miClient.showMsgBoxWarning(validation.getMessage());
            }
        }
        else {
            mnFormResult = SLibConstants.FORM_RESULT_OK;
            setVisible(false);
        }
    }

    private void actionCancel() {
        mnFormResult = SLibConstants.FORM_RESULT_CANCEL;
        setVisible(false);
    }

    private void itemStateChangedFiscalYear() {
        if (moFieldFiscalYear.getKeyAsIntArray()[0] > 0) {
            mnParamIdYear = moFieldFiscalYear.getKeyAsIntArray()[0];
        }
        else {
            mnParamIdYear = 0;
        }
    }

