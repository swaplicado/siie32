        for (int i = 0; i < mvFields.size(); i++) {
            ((erp.lib.form.SFormField) mvFields.get(i)).resetField();
        }

        moFieldPkDateStartId.setDate(miClient.getSessionXXX().getWorkingDate());

        jcbPkSalesAgentId.setEnabled(true);
